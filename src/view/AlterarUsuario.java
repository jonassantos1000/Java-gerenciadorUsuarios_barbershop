    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        
        DefaultTableModel modelo = (DefaultTableModel) gridBusca.getModel();
        modelo.setNumRows(0);
        TableColumnModel modeltable2 = gridBusca.getColumnModel();
        modeltable2.getColumn(0).setPreferredWidth(45);
        centralizar(gridBusca,0);
        centralizar(gridBusca,1);
        Usuarios usuario = new Usuarios("","","");
        usuario.selectAlteraUser();
        list=usuario.getListUsers();
        try {
            String lenlist;
            List<Usuarios> listagem = list;
            lenlist=String.valueOf(listagem.size());
            for (Usuarios user : listagem) {
                String gerencia= user.getGerencia();
                if(gerencia.equals("T")){
                    gerencia="SIM";
                }else {
                    gerencia="NAO";
                }
                
                modelo.addRow(new Object[]{user.getUsuario(),gerencia});
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao buscar os usuarios, contate o suporte t√©cnico");
        }
    }//GEN-LAST:event_btBuscarActionPerformed

