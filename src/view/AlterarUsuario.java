    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        try{
            String pass=String.valueOf(txtSenha.getPassword());
            String login=txtLogin.getText();
            if(pass.length()<4){
                JOptionPane.showMessageDialog(null, "A senha deve conter no minimo 4 digitos !");
            }else if(login.isEmpty()){
                JOptionPane.showMessageDialog(null, "Selecione o usuario que deseja alterar");
            }else{
                
                String senha = criptografar(new String(txtSenha.getPassword()));
                String ckGerencia = cbGerencia.isSelected()==false ? "F" : "T";
                Usuarios usuario = new Usuarios(login,senha,ckGerencia);
                usuario.alterar(usuario);
                this.dispose();
            }
        }catch(Exception e){

            e.printStackTrace();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void gridBuscaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gridBuscaMouseClicked
        try{
            if(evt.getClickCount() == 2){
                String user= String.valueOf(gridBusca.getValueAt(gridBusca.getSelectedRow(),0));
                String txtGerencia=String.valueOf(gridBusca.getValueAt(gridBusca.getSelectedRow(),1));
                txtLogin.setText(user);
                if(txtGerencia=="SIM"){
                cbGerencia.setSelected(true);
                }else{
                cbGerencia.setSelected(false);
                }
                BuscarUsuario.setVisible(false);                
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Selecione o usuario que deseja Alterar");
        }
    }//GEN-LAST:event_gridBuscaMouseClicked

